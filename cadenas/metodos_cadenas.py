#Metodos para las cadenas

#Convierte la cadena a mayuscula
cadena="Hola mundo".upper()
print(cadena)

#Convierte la cadena a minuscula
cadena="Hola mundo".lower()
print(cadena)

#Convierte la primerla letra de la cadena a mayuscula
cadena="hola mundo".capitalize()
print(cadena)

#Convierte la primeras letras en mayusculas
cadena="hola mundo".title()
print(cadena)

#Cuenta
cadena="hola mundo".count("o")
print(cadena)

#Buscar en la cadena indice
cadena="hola mundo".find("o")
print(cadena)

#Comprueba si solo hay caracteres numericos
cadena="Hola mundo".isdigit()
print(cadena)

#Comprueba si solo hay caracteres alfabeticos
cadena="Hola mundo 1".isalpha()
print(cadena)

#Comprueba si solo hay caracteres alfabeticos y numericos
cadena="Hola mundo 1".isalnum()
print(cadena)